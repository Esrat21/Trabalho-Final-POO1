    private void ConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmarActionPerformed
        
        ProcuraNoBanco procurando = new ProcuraNoBanco();
        String linha = null;
        File file = new File("C:\\Banco\\"+ID.getText()+".txt");
    
        if (file.exists()) {
        try {
            FileReader arq = new FileReader("C:\\Banco\\"+ID.getText()+".txt");
            BufferedReader lerArq = new BufferedReader(arq);
            
            linha = lerArq.readLine();
            linha = lerArq.readLine();
          if (Senha.getText().equals(linha)) {
              TelaDeCarregamento tela = new TelaDeCarregamento();
              tela.setVisible(true);
              tela.setLocationRelativeTo(null);
              dispose();
              
          }else {
              JOptionPane.showMessageDialog(null, "Senha Invalida", "Erro", JOptionPane.INFORMATION_MESSAGE);
          }
        } catch (FileNotFoundException ex) {
           Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }   catch (IOException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            } catch (JavaLayerException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else {
            JOptionPane.showMessageDialog(null, "ID inexistente","Erro" , JOptionPane.INFORMATION_MESSAGE);
        }
        ID.setText("");
        Senha.setText("");
    }//GEN-LAST:event_ConfirmarActionPerformed

